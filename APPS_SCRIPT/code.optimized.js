function فرض_صلاحية_الطلب(action, data) {
  if (PUBLIC_ACTIONS.has(action)) return;
  const الجلسة = جلب_جلسة_صالحة(data && data._token);
  const الأدوار_المسموحة = ACTION_ROLES[action];
  if (الأدوار_المسموحة && !الأدوار_المسموحة.includes(الجلسة.الوظيفة)) {
    throw new Error("ليس لديك صلاحية لتنفيذ هذا الإجراء");
  }
  data._session = الجلسة;
}

function إنشاء_جلسة_عامل(عامل) {
  const token = Utilities.getUuid().replace(/-/g, '') + Utilities.getUuid().replace(/-/g, '');
  const الجلسة = {
    الاسم: عامل['الاسم'] || '',
    الوظيفة: عامل['الوظيفة'] || '',
    الحلقة: عامل['الحلقة'] || '',
    وقت_الإنشاء: new Date().toISOString(),
  };
  CacheService.getScriptCache().put('sess:' + token, JSON.stringify(الجلسة), SESSION_TTL_SECONDS);
  return token;
}

function جلب_جلسة_صالحة(token) {
  if (!token) throw new Error('الجلسة غير صالحة أو انتهت');
  const raw = CacheService.getScriptCache().get('sess:' + token);
  if (!raw) throw new Error('الجلسة غير صالحة أو انتهت');
  return JSON.parse(raw);
}

function مفتاح_كاش_شيت(sheetName) {
  return 'sheet:' + sheetName;
}

function قراءة_كائنات_شيت_مع_كاش(sheetName, ttlSeconds) {
  const cache = CacheService.getScriptCache();
  const key = مفتاح_كاش_شيت(sheetName);
  const cached = cache.get(key);
  if (cached) return JSON.parse(cached);
  const sheet = SpreadsheetApp.getActiveSpreadsheet().getSheetByName(sheetName);
  const rows = _sheetToObjects(sheet);
  cache.put(key, JSON.stringify(rows), ttlSeconds || 60);
  return rows;
}

